// MARK: - SVG Path Shape

struct SvgPathShape: Shape {
    let path: String

    func path(in rect: CGRect) -> Path {
        let viewBox = CGRect(x: 0, y: 0, width: 640, height: 640)
        var p = Path()
        if let cgPath = CGPath.fromSVGPath(path) {
            var transform = CGAffineTransform(scaleX: rect.width / viewBox.width, y: rect.height / viewBox.height)
            if let transformed = cgPath.copy(using: &transform) {
                p.addPath(Path(transformed))
            }
        }
        return p
    }
}

